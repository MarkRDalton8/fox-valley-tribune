window.fvtSubscribe = function () {
  window.tp = window.tp || [];
  window.tp.push(['init', function () {
    window.tp.offer.show({ offerId: 'OFSKQS2JDQIS', displayMode: 'modal' });
  }]);
};

window.fvtSignIn = function () {
  window.tp = window.tp || [];
  window.tp.push(['init', function () {
    window.tp.pianoId.show({ screen: 'register', displayMode: 'modal' });
  }]);
};
